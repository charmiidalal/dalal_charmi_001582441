    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        /* On save button get values from textboxes and store it in variables */
        double temperature = Double.parseDouble(tempTextbox.getText());
        double bp = Double.parseDouble(bloodPressTextbox.getText());
        int pulse = Integer.parseInt(pulseTextbox.getText());
        String date = dateTextbox.getText();
        /* Add values into the Vital Sign object */
        VitalSigns v = vsh.addVitals();
        v.setBloodPressure(bp);
        v.setTemperature(temperature);
        v.setPulse(pulse);
        v.setDate(date);
        /* Show confirmation message that Vital Sign is created */
        JOptionPane.showMessageDialog(null,"Vital Signs Added Successfully.");
        /* Empty textboxes after creating the Vital Sign */
        tempTextbox.setText("");
        bloodPressTextbox.setText("");
        pulseTextbox.setText("");
        dateTextbox.setText("");
    }//GEN-LAST:event_saveButtonActionPerformed


