    private void btnDeleteCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCarActionPerformed
        // TODO add your handling code here:
        /* Onclick of delete check if row is selected */
        int selectedRow = tblCars.getSelectedRow();
        if(selectedRow >= 0)
        {
            /* Confirm the action and unset the object from array */
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete this car details?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Car car = (Car) tblCars.getValueAt(selectedRow, 0);
                carCatalog.deleteCar(car);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select row from table first!","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteCarActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        /* Check if row is selected */
        int selectedRow = tblCars.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null, "Please select row from table first!","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            /* fetch the details of the account using row number */
            Car car = (Car)tblCars.getValueAt(selectedRow,0);
            ViewCarDetailsJPanel panel = new ViewCarDetailsJPanel(menuContainer, car);
            menuContainer.add("ViewCarDetailsJPanel",panel);
            CardLayout layout = (CardLayout) menuContainer.getLayout();
            layout.next(menuContainer);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

