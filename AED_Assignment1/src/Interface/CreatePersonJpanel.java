    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        /* Check if image is added as it is a required field */
        if(person.getImagePath() == null){
            fullImgMsgLbl.setText("This field is required");
            return;
        } else {
            /* On save button click get and set info added in each textboxes */
            person.setFirstName(firstNameTxtbox.getText());
            person.setLastName(lastNameTxtbox.getText());
            person.setBirthDate(dobTxtbox.getText());
            person.setTelNum(telNumTxtbox.getText());
            person.setFaxNum(faxNumTxtbox.getText());
            person.setEmailAddress(emailAddTxtbox.getText());
            person.setStreetAddrs1(streetAddTxtbox.getText());
            person.setStreetAddrs2(streetAdd2Txtbox.getText());
            person.setCity(cityTxtbox.getText());
            person.setState(stateTxtbox.getText());
            person.setZipcode(zipcodeTxtbox.getText());
            person.setSsnNum(ssnNoTxtbox.getText());
            person.setMedicalRecNo(medicalRecNoTxtbox.getText());
            person.setHealthPlanNo(healthPlanNoTxtbox.getText());
            person.setBankAccNo(bankAccNoTxtbox.getText());
            person.setLicenseNo(licenseNoTxtbox.getText());
            person.setVehicleNo(vehicleNoTxtbox.getText());
            person.setDeviceNo(deviceNoTxtbox.getText());
            person.setLinkedIn(linkedInTxtbox.getText());
            person.setBiometric(biometricNoTxtbox.getText());
            person.setUniqueIdentityNo(uniqueIdentityNoTxtbox.getText());
            person.setIpAddr(iPAddrTxtbox.getText());
            /* Check if Save action is performed before viewing the image */
            person.setSaveFlag(true);
        }
        /* Onsaviing fields show confirmation message */
        JOptionPane.showMessageDialog(null, "Person Created Successfully.");
    }//GEN-LAST:event_saveBtnActionPerformed

    private void fullImgTxtboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fullImgTxtboxActionPerformed
        // TODO add your handling code here:
        //Create a file chooser
        final JFileChooser fc = new JFileChooser();
        //Check if file is uploaded or cancelled
        int returnVal = fc.showSaveDialog(CreatePersonJpanel.this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = fc.getSelectedFile();
                //This is where a real application would save the file.
                fullImgMsgLbl.setText("Saving: " + file.getName() + ".");
                person.setImagePath(file.getAbsolutePath());
            } else {
                fullImgMsgLbl.setText("Save command cancelled by user.");
            }
    }//GEN-LAST:event_fullImgTxtboxActionPerformed


