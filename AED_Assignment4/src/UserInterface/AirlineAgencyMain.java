    public void showAirlinerComboBox() {
        ArrayList<String> airlinerList = new ArrayList();
        DefaultComboBoxModel comboModel = new DefaultComboBoxModel();
        comboModel.addElement("Select Airliner");
        for (Airliner airliner : dirAirliner.getAirlinerDir()) {
            if (!airlinerList.contains(airliner.getAirlineName())) {
                airlinerList.add(airliner.getAirlineName());
                comboModel.addElement(airliner.getAirlineName());
            }
        }
        airlinerComboBox.setModel(comboModel);
    }

