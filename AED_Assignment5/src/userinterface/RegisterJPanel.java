    private void registerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerJButtonActionPerformed
        String username = userNameJTextField.getText();
        // Get Password
        char[] passwordCharArray1 = passwordField.getPassword();
        String password1 = String.valueOf(passwordCharArray1);
        char[] passwordCharArray2 = passwordField1.getPassword();
        String password2 = String.valueOf(passwordCharArray2);

        if (system.checkIfUserIsUnique(username)) {
            if (password1.equals(password2)) {
                this.cardLayoutJPanel.setSize(500, 430);
                CustomerAccount a =new CustomerAccount(username, password2, new Customer(username, "123@husky.neu.edu","123123"));
                CustomerAreaJPanel cp = new CustomerAreaJPanel(cardLayoutJPanel, a);
                this.cardLayoutJPanel.add("CustomerRegistrationInfoJPanel", cp);
                CardLayout layout = (CardLayout) this.cardLayoutJPanel.getLayout();
                layout.next(cardLayoutJPanel);
            } else {
                JOptionPane.showMessageDialog(null, "Passwords don't match!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Username already exists!");
        }
        DB4OUtil.getInstance().storeSystem(system);
    }//GEN-LAST:event_registerJButtonActionPerformed


